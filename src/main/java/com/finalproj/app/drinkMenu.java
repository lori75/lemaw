    private void icedTeaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_icedTeaActionPerformed
        if(evt.getSource()==icedTea){
            item = new MenuItem(drinks[3],drinkPrice[3]);
            currentOrder.addItem(item);
            int iceChoice = JOptionPane.showConfirmDialog(null,"Would you like to add additional ice?", "Ice Confirmation",JOptionPane.YES_NO_OPTION);
            dispose();
            int sugarChoice = JOptionPane.showConfirmDialog(null,"Would you like additional sugar syrup to your iced tea?", "Sugar Syrup Confirmation",JOptionPane.YES_NO_OPTION);
            
            if (iceChoice == JOptionPane.YES_OPTION && sugarChoice == JOptionPane.YES_OPTION){
                JOptionPane.showMessageDialog(null,"Added Extra Ice and Sugar Syrup to your Drink!","Drink Customization",JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }else if (iceChoice == JOptionPane.YES_OPTION && sugarChoice == JOptionPane.NO_OPTION){ 
                JOptionPane.showMessageDialog(null,"Added Extra Ice to your Drink!","Drink Customization",JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }else if (iceChoice == JOptionPane.NO_OPTION && sugarChoice == JOptionPane.YES_OPTION){ 
                JOptionPane.showMessageDialog(null,"Added Sugar Syrup to your Drink!","Drink Customization",JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }
            else{   
                JOptionPane.showMessageDialog(null,"Added Iced Tea to Orders!","Order Confirmation",JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }
            dispose();
            new viewMenu().setVisible(true);
        }
    }//GEN-LAST:event_icedTeaActionPerformed

