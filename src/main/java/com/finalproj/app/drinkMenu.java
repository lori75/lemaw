    private void waterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_waterActionPerformed
        if(evt.getSource()==water){
            item = new MenuItem(drinks[0],drinkPrice[0]);
            currentOrder.addItem(item);
            int iceChoice = JOptionPane.showConfirmDialog(null,"Would you like to add additional ice?", "Ice Confirmation",JOptionPane.YES_NO_OPTION);
            if (iceChoice == JOptionPane.YES_OPTION){
                JOptionPane.showMessageDialog(null,"Added Extra Ice!","Drink Customization",JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }else{  
                JOptionPane.showMessageDialog(null,"Added Water to Orders!","Order Confirmation",JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }
            
            dispose();
            new viewMenu().setVisible(true);
        }
    }//GEN-LAST:event_waterActionPerformed

